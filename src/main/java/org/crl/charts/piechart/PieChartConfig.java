package org.crl.charts.piechart;

import org.crl.imagedata.Color;

public class PieChartConfig {
	/**
	 * The chart color to be identified
	 * */
	private Color chartColor;

	public PieChartConfig() {
	}

	public Color getChartColor() {
		return chartColor;
	}

	public void setChartColor(Color chartColor) {
		this.chartColor = chartColor;
	}
}
